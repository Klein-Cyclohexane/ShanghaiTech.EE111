% solve_3eq_phasor.m
% 解三元一次方程组，并将解转换为相位角形式 (幅值∠角度)
% 适用于复数系数的电路方程（如网孔电流法、节点电压法）

clear; clc;

fprintf('===== 三元一次方程组求解器（相位角形式）=====\n\n');

% ---------- 用户输入区 ----------
% 修改这里的矩阵 A 和向量 b
% 方程组形式: A * [x1; x2; x3] = b
%
% 示例（请替换为实际方程）:
%    (1+2j)*x1 + (3-1j)*x2 + (0+4j)*x3 = 10∠30° = 10*exp(1j*pi/6)
%    (2+0j)*x1 + (0+2j)*x2 + (1-1j)*x3 = 5∠0°   = 5
%    (0-1j)*x1 + (4+0j)*x2 + (2+3j)*x3 = 8∠-45° = 8*exp(-1j*pi/4)

A = [20+30j,  -30j,  0;
     -30j,  -215j,  250j;
     0,  250j,  -227j];

b = [120j;
     0;
     -80];

% ---------- 求解 ----------
x = A \ b;   % 求解复数的 x1, x2, x3

% ---------- 转换为相位角形式 ----------
mag   = abs(x);      % 幅值
phase = angle(x);    % 相位角 (弧度)
phase_deg = phase * 180 / pi;  % 相位角 (度)

% ---------- 输出结果 ----------
fprintf('解（直角坐标形式）:\n');
for k = 1:3
    fprintf('  x%d = %.4f %+.4fj\n', k, real(x(k)), imag(x(k)));
end

fprintf('\n解（相位角形式 / 极坐标形式）:\n');
for k = 1:3
    fprintf('  x%d = %.4f ∠ %.2f°\n', k, mag(k), phase_deg(k));
end

fprintf('\n========================================\n');

% ---------- 验证 ----------
fprintf('\n验证: 将解代回原方程\n');
b_check = A * x;
for k = 1:3
    fprintf('  方程%d: 左边 = %.4f∠%.2f°  vs  右边 = %.4f∠%.2f°\n', ...
        k, abs(b_check(k)), angle(b_check(k))*180/pi, ...
        abs(b(k)), angle(b(k))*180/pi);
end
